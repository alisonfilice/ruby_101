def tricky_method(a_string_param, an_array_param)
  a_string_param += "rutabaga"
  an_array_param << "rutabaga"
end

my_string = "pumpkins"
my_array = ["pumpkins"]
tricky_method(my_string, my_array)

puts "My string looks like this now: #{my_string}" #display will not include rutabage, because + does not mutate the string
puts "My array looks like this now: #{my_array}" #<< does mutate the array, so you rutabaga will be added

#4. Let's try to modify the string, but not the array
def tricky_method_two(a_string_param, an_array_param)
  a_string_param << 'rutabaga' #<< modifies the string, so keeps the object id
  an_array_param = ['pumpkins', 'rutabaga'] #this just reassigns the variable so it gets a new object id, but is not modified
end

my_string = "pumpkins"
my_array = ["pumpkins"]
tricky_method_two(my_string, my_array)

puts "My string looks like this now: #{my_string}"
puts "My array looks like this now: #{my_array}"

#5. How can we change this code to make the result easier to predict and easier for the next programmer to maintain?
def not_so_tricky_method(a_string_param, an_array_param)
  a_string_param += "rutabaga"
  an_array_param += ["rutabaga"]

  return a_string_param, an_array_param
end

my_string = "pumpkins"
my_array = ["pumpkins"]
my_string, my_array = not_so_tricky_method(my_string, my_array)

puts "My string looks like this now: #{my_string}"
puts "My array looks like this now: #{my_array}"

